package alishev.Cycles;

/**
 * данный цикл работает до заданного предела
 */
public class For {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) { // обозначаем переменную, задаем условие для вывода, обозначаем действие с переменной в следеющий раз
            System.out.println("to day is " + i); //выводим итоговый текст после одной операции из цикла
        }
    }
}
